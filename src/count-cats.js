/**
 * Given matrix where you have to find cats by ears "^^"
 *
 * @param {Array<Array>} matrix 
 * @return {Number} count of cats found
 *
 * @example
 * countCats([
 *  [0, 1, '^^'],
 *  [0, '^^', 2],
 *  ['^^', 1, 2]
 * ]) => 3`
 *
 */
export default function countCats(arr) {
  let sum = arr.reduce((count, item) => {
    let x =  item.filter(item => item === '^^');
      count += x.length;
    
    return count;
  },0);
  return sum;
}
